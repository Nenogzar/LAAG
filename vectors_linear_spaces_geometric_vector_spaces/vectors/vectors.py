"""
Свободен вектор е вектор, който не е свързан с конкретна начална точка в пространството.
Той се характеризира само със своите дължина (модул) и посока.
Свободният вектор може да бъде преместен успоредно на себе си в пространството без да се променят неговите характеристики.

### Основни характеристики на свободния вектор:
1. **Дължина (модул)**: Дължината на вектора, обозначена като \( \| \vec{v} \| \),
представлява разстоянието от началната до крайната точка на вектора.
2. **Посока**: Посоката на вектора показва в коя посока той "сочи".
3. **Ориентация**: Векторите имат ориентация, което означава,
че те не само сочат в определена посока, но и имат "преден" и "заден" край.

### Формално определение:
Свободен вектор е наречен вектор, ако е определен само от своята големина и посока, без фиксирано начало.
Два свободни вектора са равни, ако имат една и съща дължина и посока, независимо от местоположението им в пространството.

### Примери за свободни вектори:
1. **Вектор на скорост**:
Ако даден обект се движи с определена скорост и посока, неговата скорост може да се представи като свободен вектор.
2. **Вектор на сила**:
Силата, приложена върху тяло, може да се представи като свободен вектор, тъй като се характеризира с величина и посока.

### Разлика между свободен и приложен (закрепен) вектор:
- **Свободен вектор**:
Може да се премества навсякъде в пространството, без да променя своите характеристики.
- **Приложен (закрепен) вектор**:
Има фиксирана начална точка и промяната на местоположението му води до промяна на вектора.

### Представяне на свободен вектор:
Свободният вектор често се представя чрез двойка точки в пространството \((A, B)\),
където векторът \(\vec{AB}\) е свободен вектор с начало в точка \(A\) и край в точка \(B\).
Математически, това може да се запише като разликата на координатите на точките \(B\) и \(A\):

\[ \vec{AB} = (x_B - x_A, y_B - y_A) \]

Свободните вектори са фундаментална концепция в линейната алгебра и векторния анализ,
използвани широко в физиката, инженерството и математиката.

"""

###  Изчисляване на дължина на вектори ###


import math


def calculate_vector_length():
    # Избор за вида на вектора
    dimension = input("Изберете типа на вектора (2D или 3D): ").strip().lower()

    if dimension == "2d":
        # Въвеждане на координати за двумерен вектор
        x1, y1 = map(float, input("Въведете координатите на началната точка (x1, y1): ").split())
        x2, y2 = map(float, input("Въведете координатите на крайната точка (x2, y2): ").split())

        # Изчисляване на дължината на вектора
        length = math.sqrt((x2 - x1) ** 2 + (y2 - y1) ** 2)

        print(f"Дължината на двумерния вектор е: {length}")

    elif dimension == "3d":
        # Въвеждане на координати за тримерен вектор
        x1, y1, z1 = map(float, input("Въведете координатите на началната точка (x1, y1, z1): ").split())
        x2, y2, z2 = map(float, input("Въведете координатите на крайната точка (x2, y2, z2): ").split())

        # Изчисляване на дължината на вектора
        length = math.sqrt((x2 - x1) ** 2 + (y2 - y1) ** 2 + (z2 - z1) ** 2)

        print(f"Дължината на тримерния вектор е: {length}")

    else:
        print("Невалиден избор. Моля, изберете 2D или 3D.")


# Извикване на функцията за изчисляване на дължината на вектора
calculate_vector_length()

